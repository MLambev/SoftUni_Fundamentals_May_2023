coffee_counter = 0

while True:
    event = input()

    if event == "END":
        break

    if event == "coding" or event == "dog" or event == "cat" or event == "movie":
        coffee_counter += 1
    elif event == "CODING" or event == "DOG" or event == "CAT" or event == "MOVIE":
        coffee_counter += 2
    else:
        continue

    if coffee_counter > 5:
        print("You need extra sleep")
        break

if coffee_counter <= 5:
    print(coffee_counter)
